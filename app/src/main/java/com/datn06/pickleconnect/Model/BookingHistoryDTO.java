package com.datn06.pickleconnect.Model;

import java.math.BigDecimal;
import java.util.List;

public class BookingHistoryDTO {
    private Long bookingId;
    private String bookingCode;
    private String bookingDate; // LocalDateTime from backend -> String in Android
    private String userName;
    private String userMobile;
    private String userEmail;
    private String status;
    private BigDecimal totalHours;
    private BigDecimal totalPrice;
    private String facilityName;
    private String streetAddress;
    private String ward;
    private String district;
    private String province;
    private List<FieldTimeSlotDTO> slots;

    // Getters and Setters
    public Long getBookingId() {
        return bookingId;
    }

    public void setBookingId(Long bookingId) {
        this.bookingId = bookingId;
    }

    public String getBookingCode() {
        return bookingCode;
    }

    public void setBookingCode(String bookingCode) {
        this.bookingCode = bookingCode;
    }

    public String getBookingDate() {
        return bookingDate;
    }

    public void setBookingDate(String bookingDate) {
        this.bookingDate = bookingDate;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserMobile() {
        return userMobile;
    }

    public void setUserMobile(String userMobile) {
        this.userMobile = userMobile;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public BigDecimal getTotalHours() {
        return totalHours;
    }

    public void setTotalHours(BigDecimal totalHours) {
        this.totalHours = totalHours;
    }

    public BigDecimal getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(BigDecimal totalPrice) {
        this.totalPrice = totalPrice;
    }

    public String getFacilityName() {
        return facilityName;
    }

    public void setFacilityName(String facilityName) {
        this.facilityName = facilityName;
    }

    public String getStreetAddress() {
        return streetAddress;
    }

    public void setStreetAddress(String streetAddress) {
        this.streetAddress = streetAddress;
    }

    public String getWard() {
        return ward;
    }

    public void setWard(String ward) {
        this.ward = ward;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public List<FieldTimeSlotDTO> getSlots() {
        return slots;
    }

    public void setSlots(List<FieldTimeSlotDTO> slots) {
        this.slots = slots;
    }

    // Helper method to get full address
    public String getFullAddress() {
        return streetAddress + ", " + ward + ", " + district + ", " + province;
    }
}
